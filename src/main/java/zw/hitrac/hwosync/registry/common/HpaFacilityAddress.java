package zw.hitrac.hwosync.registry.common;

public class HpaFacilityAddress {

  private String addressType;
  private String address1;
  private String address2;
  private String HpaFacilityAddressId;

  public String getAddressType () {
    return addressType;
  }

  public void setAddressType (String addressType) {
    this.addressType = addressType;
  }

  public String getAddress1 () {
    return address1;
  }

  public void setAddress1 (String address1) {
    this.address1 = address1;
  }

  public String getAddress2 () {
    return address2;
  }

  public void setAddress2 (String address2) {
    this.address2 = address2;
  }

  public String getHpaFacilityAddressId () {
    return HpaFacilityAddressId;
  }

  public void setHpaFacilityAddressId (String HpaFacilityAddressId) {
    this.HpaFacilityAddressId = HpaFacilityAddressId;
  }


}
