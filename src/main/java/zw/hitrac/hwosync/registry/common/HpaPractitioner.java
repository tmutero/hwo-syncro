package zw.hitrac.hwosync.registry.common;

public class HpaPractitioner {
  private String firstName;
  private String lastName;
  private String contact;
  private String registrationNumber;
  private String hpaPractitionerId;

  public String getFirstName () {
    return firstName;
  }

  public void setFirstName (String firstName) {
    this.firstName = firstName;
  }

  public String getLastName () {
    return lastName;
  }

  public void setLastName (String lastName) {
    this.lastName = lastName;
  }

  public String getContact () {
    return contact;
  }

  public void setContact (String contact) {
    this.contact = contact;
  }

  public String getRegistrationNumber () {
    return registrationNumber;
  }

  public void setRegistrationNumber (String registrationNumber) {
    this.registrationNumber = registrationNumber;
  }

  public String getHpaPractitionerId () {
    return hpaPractitionerId;
  }

  public void setHpaPractitionerId (String hpaPractitionerId) {
    this.hpaPractitionerId = hpaPractitionerId;
  }


}
